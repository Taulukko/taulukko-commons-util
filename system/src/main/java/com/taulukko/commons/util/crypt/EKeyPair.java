package com.taulukko.commons.util.crypt;

public class EKeyPair
{
	
	private String privateKey64 =  null;
	private String publicKey64 =  null;
	
	public String getPrivateKey64()
	{
		return privateKey64;
	}
	public void setPrivateKey64(String privateKey64)
	{
		this.privateKey64 = privateKey64;
	}
	public String getPublicKey64()
	{
		return publicKey64;
	}
	public void setPublicKey64(String publicKey64)
	{
		this.publicKey64 = publicKey64;
	}
	

}
